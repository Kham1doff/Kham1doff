topishmoq_raqam = 3
for urinish in range (10):
    print(urinish +1, "-urinish")
    oyinchi_raqami = int(input("Raqamingizni kiriting: "))
    if oyinchi_raqami > topishmoq_raqam:
        print("Mening oylagan raqamim kichkina")
        
    elif oyinchi_raqami < topishmoq_raqam:
        print("Men oylagan raqam katta")
        
    elif oyinchi_raqami == topishmoq_raqam:
        print("Siz yutdingiz! Tabriklayman! Men oylagan raqamim: ", topishmoq_raqam)
        
    elif oyinchi_raqami != oyinchi_raqami:
        print("Afsuski siz yutqazdingiz!")




    